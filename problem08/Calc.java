class Calc{
	



	
	//int 숫자로 적용하게만든다.
	// add 더하기 sub 빼기
	// mul 곱하기 div 나누기
	//메서드
	int add(int a, int b){
		int aaa = a + b;
		return aaa;
	}
	//메서드
	// int sub(000, 000) 000에들어가는것은 임의의 변수
	int sub(int a, int b){
		int aaa = a - b;
		return aaa;
	}
	//메서드
	// return은 출력하는부분이다.
	int mul(int a, int b){
        int aaa = a * b;
		return aaa;
	}
	//메서드
	int div(int a, int b){
		int aaa = a / b;
		return aaa;
	}
}