public class Java{
	public static void main(String[] args){
		
		
		//'H'를 출력
		for (char i = 65; i < 91; i++){
			if(i == 'H'){
				System.out.print(i);
				break;
			}
		}
		//'E'를 출력
		for (char i = 65; i < 91; i++){
			if(i == 'E'){
				System.out.print(i);
				break;
			}
		}
		//'L'을 출력
		for (char i = 65; i < 91; i++){
			if(i == 'L'){
				System.out.print(i);
				System.out.print(i);
				break;
			}
		}
	
		//'O'를 출력
		for (char i = 65; i < 91; i++){
			if(i == 'O'){
				System.out.print(i);
				break;
			}
		}
	}
	//A~Z까지 읽다가 조건에맞으면 종료가된다.
}